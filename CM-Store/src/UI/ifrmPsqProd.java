    public void list_prod() {
        String sql = "Select codigo, codbarras, nome, prc_venda, estoque from produto";
        PreparedStatement ps;
        ResultSet rs;
        try {
            ps = Conexao.getConexao().prepareStatement(sql);
            rs = ps.executeQuery();
            tab_prod.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (ClassNotFoundException | SQLException error) {
            JOptionPane.showMessageDialog(null, error, "CM - Store 1.0 | Erro - Pesquisa de Produtos Simplificada", JOptionPane.ERROR_MESSAGE, erro);
        }
    }

    public void psq_produto() {
        String sql = "Select codigo, codbarras, nome, prc_venda, estoque from produto where codigo like ? or codbarras like ?or nome like ?";
        PreparedStatement ps;
        ResultSet rs;
        try {
            ps = Conexao.getConexao().prepareStatement(sql);
            ps.setString(1, psq_prod.getText() + "%");
            ps.setString(2, psq_prod.getText() + "%");
            ps.setString(3, psq_prod.getText() + "%");
            rs = ps.executeQuery();
            tab_prod.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (ClassNotFoundException | SQLException error) {
            JOptionPane.showMessageDialog(null, error, "CM - Store 1.0 | Erro - Pesquisa de Produtos Simplificada", JOptionPane.ERROR_MESSAGE, erro);
        }
    }

    public void comp_ifrm() {
        int seleciona = tab_prod.getSelectedRow();
        cod.setText(tab_prod.getModel().getValueAt(seleciona, 0).toString());
        codbar.setText(tab_prod.getModel().getValueAt(seleciona, 1).toString());
        nome.setText(tab_prod.getModel().getValueAt(seleciona, 2).toString());
        vlr.setText(tab_prod.getModel().getValueAt(seleciona, 3).toString());
        est.setText(tab_prod.getModel().getValueAt(seleciona, 4).toString());
    }

